  function hardenCase(data){
    const next = Object.assign({}, data, { stages:{} });
    Object.entries(data.stages || {}).forEach(([stageKey, stage]) => {
      const correct = (stage.options || []).find((option) => option.correct) || stage.options?.[0];
      const misses = nearMisses(stageKey, data).map((option) => Object.assign({}, option, { correct:false }));
      const prompt = `${String(stage.prompt || '').replace(/\s+$/, '')}\n\nเลือกคำตอบที่อธิบาย “ข้อมูลในคดีนี้” ได้ดีที่สุด ไม่ใช่คำตอบที่ดูดีในภาพรวม`;
      next.stages[stageKey] = Object.assign({}, stage, {
        prompt,
        options: shuffle([correct, ...misses].filter(Boolean))
      });
    });
    return next;
  }

  function harden(config){
    if (!config || typeof config !== 'object') return config;
    const next = Object.assign({}, config);
    next.bank = (config.bank || []).map(hardenCase);
    if (Array.isArray(config.bossBank)) next.bossBank = config.bossBank.map(hardenCase);
    next.recentLimit = Math.max(Number(config.recentLimit || 0), missionId === 'b1' || missionId === 'b2' ? 8 : 6);
    next.intro = `${String(config.intro || '')} ทุกคำตอบมีตัวลวงเชิงเหตุผลที่ใกล้เคียงกัน จึงต้องอ่านบริบทและ Reason Check ก่อนตัดสินใจ`;
    next.passText = '≥ 2★ เพื่อปลดล็อก • 3★ ต้องชนะตัวลวงเชิงเหตุผลด้วยหลักฐานและ Reason Check';
    return next;
  }

