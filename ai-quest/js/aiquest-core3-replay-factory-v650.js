      const caseTwists=pick(tPool,new Set([...used,...mechanics.map(x=>x.id),...knowledge.map(x=>x.id)]),2);caseTwists.forEach(x=>x.phase='Phase 3 • Case Twist');
      cards=[...shuffle(mechanics),...shuffle(knowledge),...shuffle(caseTwists)];
