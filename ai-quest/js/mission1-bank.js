function getM1SeenState(){
  try{
    const raw = localStorage.getItem(CSAI2102_M1_SEEN_KEY);
    if(!raw) return defaultM1SeenState();

    const parsed = JSON.parse(raw);

    if(parsed.version !== CSAI2102_M1_BANK_VERSION){
      return defaultM1SeenState();
    }

    return {
      ...defaultM1SeenState(),
      ...parsed
    };
  }catch(error){
    return defaultM1SeenState();
  }
}

function defaultM1SeenState(){
  return {
    version:CSAI2102_M1_BANK_VERSION,
    classification:[],
    explain:[],
    boss:[],
    roundsPlayed:0,
    lastRoundIds:[]
  };
}

function saveM1SeenState(seen){
  localStorage.setItem(CSAI2102_M1_SEEN_KEY, JSON.stringify(seen));
}

function resetM1SeenByType(seen, type){
  seen[type] = [];
  saveM1SeenState(seen);
}

function pickNoRepeat(pool, type, count, options = {}){
  const seen = getM1SeenState();
  const memorySize = options.memorySize || 72;
  const recentlySeen = new Set((seen[type] || []).slice(-memorySize));

  let available = pool.filter(item => !recentlySeen.has(item.id));

  if(available.length < count){
    const shortMemory = new Set((seen[type] || []).slice(-Math.max(10, Math.floor(memorySize / 3))));
    available = pool.filter(item => !shortMemory.has(item.id));
  }

  if(available.length < count){
    resetM1SeenByType(seen, type);
    available = [...pool];
  }

  const selected = shuffle(available).slice(0, count);
  const updatedSeen = getM1SeenState();

  updatedSeen[type] = [
    ...(updatedSeen[type] || []),
    ...selected.map(item => item.id)
  ].slice(-memorySize);

  updatedSeen.lastRoundIds = [...selected.map(item => item.id)];
  saveM1SeenState(updatedSeen);

  return selected;
}

function buildMission1Round(difficulty = 'normal'){
  const diff = difficulty || 'normal';

  const aiCards = M1_CLASSIFICATION_CARDS.filter(item => item.group === 'AI');
  const maybeCards = M1_CLASSIFICATION_CARDS.filter(item => item.group === 'Maybe AI');
  const notCards = M1_CLASSIFICATION_CARDS.filter(item => item.group === 'Not AI');
  const trickCards = M1_CLASSIFICATION_CARDS.filter(item => item.trick);

  const rushCount = diff === 'challenge' ? 9 : diff === 'easy' ? 6 : 8;
  const trickCount = diff === 'challenge' ? 4 : 3;
  const explainCount = diff === 'challenge' ? 4 : 3;
  const bossCount = diff === 'challenge' ? 5 : 4;

  const balancedRush = [
    ...pickNoRepeat(aiCards, 'classification', Math.ceil(rushCount / 3), { memorySize:80 }),
    ...pickNoRepeat(maybeCards, 'classification', Math.ceil(rushCount / 3), { memorySize:80 }),
    ...pickNoRepeat(notCards, 'classification', Math.ceil(rushCount / 3), { memorySize:80 })
  ].slice(0, rushCount);

  const round = {
    rush: shuffle(balancedRush),
    tricks: pickNoRepeat(trickCards, 'classification', trickCount, { memorySize:80 }),
    explains: pickNoRepeat(M1_EXPLAIN_QUESTIONS, 'explain', explainCount, { memorySize:64 }),
    boss: pickNoRepeat(M1_BOSS_CLAIMS, 'boss', bossCount, { memorySize:40 })
  };

  const seen = getM1SeenState();

  seen.roundsPlayed = (seen.roundsPlayed || 0) + 1;
  seen.lastRoundIds = [
    ...round.rush.map(item => item.id),
    ...round.tricks.map(item => item.id),
    ...round.explains.map(item => item.id),
    ...round.boss.map(item => item.id)
  ];

  saveM1SeenState(seen);

  return round;
}

